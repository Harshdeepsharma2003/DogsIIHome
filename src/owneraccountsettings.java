    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed

         SessionFactory sf=new Configuration().configure().buildSessionFactory();

        Session session=sf.openSession();
        Transaction tx=session.beginTransaction();

        String name=new String(jTextField8.getName());
        String contact=jTextField2.getText();
         String shelterName=new String(jTextField3.getName());
       String address=new String(jTextArea1.getName());
        
         Shelterowner ref = (Shelterowner)session.get( Shelterowner.class,contact);
        if(ref==null){
             JOptionPane.showMessageDialog(this, "Shelter not found!");

        }
        else{
                
                    ref.setName(name);
                     ref.setContact(contact);
                     ref.setSheltername(shelterName);
                      ref.setAddress(address);
                       
                   session.update(ref);
                    tx.commit();
                    session.close();
                    JOptionPane.showMessageDialog(this, "Updated Successfully!!");
                    new Publichome().setVisible(true);
                    dispose();
                }
        
    }//GEN-LAST:event_jButton6ActionPerformed

